public class hello20 {
    public static void main(String[] args) {

        String str;
        String cur;

        str = "10 + 10 + 円 : ";
        cur = "円";
        System.out.print(str);
        System.out.println(10 + 10 + cur);

        str = "$ + 10 + 10 : ";
        cur = "$";
        System.out.print(str);
        System.out.println(cur + 10 + 10);
    }

}
